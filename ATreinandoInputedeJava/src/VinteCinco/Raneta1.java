package VinteCinco;

public class Raneta1 {
	private String modelo;
	private float ponta;

	public Raneta1(String modelo, float ponta) {
		super();
		this.ponta = ponta;
		this.modelo = modelo;
	}
	public Raneta1() {
		super();
	}

	public float getPonta() {
		return ponta;
	}

	public void setPonta(float ponta) {
		this.ponta = ponta;
	}

	public String getModelo() {
		return modelo;
	}

	public void setModelo(String modelo) {
		this.modelo = modelo;
	}



}

