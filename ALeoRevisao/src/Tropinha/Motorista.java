package Tropinha;

public class Motorista {
	private String nome;

	public Motorista(String nome) {
		super();
		this.nome = nome;
	}

	public Motorista() {
		super();
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	
	

}
