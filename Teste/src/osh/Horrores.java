package osh;

public class Horrores {
	public String tipo;
	public int valor;
	
	public Horrores() {
		
	}

	public Horrores(String tipo, int valor) {
		super();
		this.tipo = tipo;
		this.valor = valor;
	}

	public Horrores(String tipo) {
		super();
		this.tipo = tipo;
	}
	
	
}
